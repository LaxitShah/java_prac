package doubts;

public class AdminBean {


		private int Id;
		private String Name;
		private String Email;
		
		public void setId(int id) {
			this.Id = id;
		}
		
		public int getId() {
			return Id;
		}
		
		public void setName(String Name) {
			this.Name = Name;
		}
		
		public String getName() {
			return Name;
		}
		
		public void setEmail(String Email) {
			this.Email = Email;
		}
		
		public String getEmail() {
			return Email;
		}

}
